package com.kidd.projectbase.network.response;

import java.util.List;

public class BaseResponse<T> {
    private int status;
    private List<T> data;

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public List<T> getData() {
        return data;
    }

    public void setData(List<T> data) {
        this.data = data;
    }
}
